package basicOfjava;

public class dataType {
      public static void main(String[] args) {
        boolean b = true;
        byte by = 10;
        short s = 200;
        int i = 1000;
        long l = 9999999L;
        float f = 3.14f;
        double d = 9.81;
        char c = 'A';

        System.out.println(b + " " + by + " " + s + " " + i + " " + l + " " + f + " " + d + " " + c);
    }
    
}

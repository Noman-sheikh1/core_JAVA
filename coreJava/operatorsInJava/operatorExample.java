// package coreJava.operatorsInJava;
 
public class operatorExample{
    public static void main(String[] args) {
        int a=20;
        int b=3;
        System.out.println(a+b);
        System.out.println(a-b);
        //relational operators
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>b);
    }
 }